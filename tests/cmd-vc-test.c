#define compare_n_sshbuf(y, str) do {          \
    struct sshbuf *sbuf = sshbuf_new();                 \
    sshbuf_reset(sbuf);                                 \
    uints_to_sshbuf(vc->vc_uni_lines[y], vc->vc_cols + 1, sbuf);      \
    ck_assert_msg(strncmp(sshbuf_ptr(sbuf), STR_LEN(str)) == 0, "sshbuf[%s] != exp["str"]", sshbuf_ptr(sbuf)); \
    sshbuf_free(sbuf);                                  \
} while(0)

